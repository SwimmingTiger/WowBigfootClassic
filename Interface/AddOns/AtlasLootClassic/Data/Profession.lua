function Profession.GetCraftSpellForCreatedItem(itemID)
    if not CraftedToSpellID then
        CraftedToSpellID = {}
        for craftSpell, craftData in pairs(PROFESSION) do
            if craftData[1] then
                CraftedToSpellID[craftData[1]] = craftSpell
            end
        end
    end
    return CraftedToSpellID[itemID]
end

